package Day_11_Java_Logical_Operators;

public class _6_If_Kosulu {
    public static void main(String[] args) {

        // Algoritmada SART , KOSUL belirmeler vardi.


     //   EGER kosulunu ifade eden JAVA syntex  if kosulur.

      //  kosullu ifadelerde kosulu direkt olarak test eden bir tanimlamadir.

        int sayi1=13;
        int sayi2=13;

        if (sayi1/sayi2==1)
        { // Eger sayi1 in sayi 2 ye bolumu 1 e esit ise.
          // Burada IF kosulu saglanmasi durumunda yapilabilecekleri gosterir;
          // islemin on sarti gibi dusunulebilir.
            sayi2*=sayi1; // sayi2 esittir sayi1 carpi sayi2
            System.out.println(sayi2);

        };


    }
}
