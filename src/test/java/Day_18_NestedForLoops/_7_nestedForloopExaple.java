package Day_18_NestedForLoops;

public class _7_nestedForloopExaple {
    public static void main(String[] args) {

        for (int i=1; i<=5;i++){
            for (int j =1; j<=4; j++){
                System.out.println("i : "+ i + " j :"+ j);
            }
            System.out.println();
        }
    }
}
